fun main() {
    // Создаем каталог товаров
    val catalog = mapOf(
        "Конструкторы" to listOf("LEGO City", "LEGO Technic", "Магнитный конструктор", "Деревянные кубики"),
        "Куклы" to listOf("Барби", "Пупс", "Кукла-принцесса", "Интерактивная кукла"),
        "Машинки" to listOf("Гоночная машина", "Грузовик", "Полицейская машина")
    )

    // Выводим заголовок каталога
    println("=== КАТАЛОГ МАГАЗИНА ИГРУШЕК ===")
    println()

    // Внешний цикл проходит по категориям
    for ((categoryName, itemsList) in catalog) {
        println("Категория: $categoryName")

        // Внутренний цикл проходит по элементам списка конкретной категории
        for (item in itemsList) {
            println("- $item")
        }

        println()  // Добавляем пустую строку для разделения категорий
    }
}